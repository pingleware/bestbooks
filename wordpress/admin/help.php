	<h3>Perpertual Inventory System</h3>
	<ul>
	<li>no journal entry is prepared</li>
	<li>inventory and cost of goods sold accounts are updated</li>
	<li>continuously during the period</li>
	</ul>
	<h3>Periodic Inventory System</h3>
	<ul>
	<li>Beginning inventory = 400 units x $10 = $4,000</li>
	<li>Purchases = 500 unit x $10 + 600 units x $10 = $11,000</li>
	<li>Cost of goods sold = 700 units x $10 =$7,000</li>
	<li>Ending inventory = 800 units x $10 = $8,000</li>
	</ul>
	<p>Cost of Goods Sold account is debited (balance is increasing)</p>
	<p>Purchase accoount is credited (balance is increasing)</p>
	<br/>
	<p>Beginning inventory and purchases are redistributed to cost of goods sold and ending inventory</p>
	<p>Inventory is credited by the ending inventory amount (balance is increasing)</p>
	<p>Inventory is debited by the beginning inventory amount (balance is decreasing)</p>
