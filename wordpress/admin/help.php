	<h2 id="debit-accounts">Debit Accounts: Assets & Expenses</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-basics-debits-credits.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-basics-debits-credits.shtml</a>
	<p>Because Asset and Expense accounts maintain positive balances, they are positive, or debit accounts. 
	Accounting books will say “Accounts that normally have a positive balance are increased with a Debit and decreased with a Credit.” 
	Of course they are! Look at the number line. If you add a positive number (debit) to a positive number, you get a bigger positive number. 
	But if you start with a positive number and add a negative number (credit), you get a smaller positive number (you move left on the number line). 
	The asset account called Cash, or the checking account, is unique in that it routinely receives debits and credits, but its goal is to maintain a positive balance!
	</p>
	<h2 id="credit-accounts">Credit Accounts: Liabilities, Equity, & Revenue</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-basics-debits-credits.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-basics-debits-credits.shtml</a>
	<p>Liability, Equity, and Revenue accounts usually receive credits, so they maintain negative balances. 
	They are called credit accounts. Accounting books will say “Accounts that normally maintain a negative balance are increased with a Credit and decreased with a Debit.” 
	Again, look at the number line. If you add a negative number (credit) to a negative number, you get a larger negative number! (moving left on the number line). 
	But if you start with a negative number and add a positive number to it (debit), you get a smaller negative number because you move to the right on the number line.</p>
	<p>We have not discussed crossing zero on the number line. If we have $100 in our checking account and write a check for $150, 
	the check will bounce and Cash will have a negative value - an undesirable event. A negative account might reach zero - such as a loan account when the final payment is posted. 
	And many accounts, such as Expense accounts, are reset to zero at the beginning of the new fiscal year. But credit accounts rarely have a positive balance and debit accounts rarely have a negative balance at any time.</p>
	<p>[Remember: A debit adds a positive number and a credit adds a negative number. But you NEVER put a minus sign on a number you enter into the accounting software.]</p>
	<h2 id="example-1">Example 1: Owner Invests Capital in the Company</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-transactions.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-transactions.shtml</a>
	<p>Owner invests $5,000.<p>
	<p>Analysis: Since money is deposited into the checking account, Cash is debited (the balance increased by $5,000).<p>
	<p>What account receives a credit?<p>
	<p>An Equity account called Owner’s Equity or Capital Contribution. Since Equity accounts are ‘negative’ accounts, crediting this Equity account increases its balance by $5,000.</p>
	<p>Debit Cash (increase its balance)</p>
	<p>Credit Owner’s Equity|Capital (increases its balance)</p>
	<h2 id="example-2">Example 2: Company Takes Out a Loan</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-transactions.shtml" target="">From: https://www.keynotesupport.com/accounting/accounting-transactions.shtml</a>
	<p>The company borrows $8,000 from a bank.<p>
	<p>Analysis: Since the money will be deposited into the checking account, Cash is debited (the balance increased by $8,000). 
	The account to receive the credit is a Liability account called Loans Payable (you may create a separate account or sub-account for each loan). 
	Liability accounts are credit accounts, so crediting the Liability account increases its negative balance by $8,000 (moves to the left on the number line).</p>
	<p>Debit Cash (increases its balance)</p>
	<p>Credit Loans Payable (increases its balance)</p>
	<h2 id="example-3">Example 3: Monthly Statement Fee from Bank</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-transactions.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-transactions.shtml</a>
	<p>Your bank charges a monthly statement fee of $14.   Analysis: This transaction is entered via a journal entry each month when the checking account is balanced. 
	Since money was removed from the checking account, Cash is credited (the balance decreased by $14). 
	The Expense account called Bank Service Charges receives the debit.</p>
	<p>Debit Bank Fees (increases its balance)</p>
	<p>Credit Cash (decreases its balance)</p>
	<h2 id="example-4">Example 4: Making a Loan Payment</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-transactions.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-transactions.shtml</a>
	<p>You pay $540, via check, on the $8,000 loan acquired in Example 2. Of this amount, $500 is applied to the principal, and $40 is applied to the loan interest.</p>
	<p>Analysis: Since a check is being written, BestBooks will automatically credit Cash. 
	In this case the debit is split between two accounts. To reflect the $500 that has been applied to the loan balance, debit the loan account. 
	(Since it is a liability account, a debit will reduce its balance, which is what you want). 
	The $40 interest paid is an expense, so debit the expense account called Loan Interest. 
	Remember that even though the debit is split between two accounts, the total debit must always equal the total credit.</p>
	<p>Debit Loans Payable $500 (decreases its balance)</p>
	<p>Debit Interest Expense $40 (increases its balance)</p>
	<p>Credit Cash $540 (decreases its balance)</p>
	<h2 id="example-5">Example 5: Company Writes a Check to Pay for an Asset</h2>
	<a href="https://www.keynotesupport.com/accounting/accounting-transactions.shtml" target="_blank">From: https://www.keynotesupport.com/accounting/accounting-transactions.shtml</a>
	<p>The Company writes a check for $8,500 of equipment.</p>
	<p>Analysis: Since a check was written, BestBooks will automatically credit Cash. 
	The item is too costly to be considered an expense, so it must be entered into the accounting system as an asset. 
	So we will debit an Asset account called Equipment or something similar. 
	In addition, assets must be depreciated over time, with journal entries entered each year for a proscribed number of years. 
	Depreciation is complicated, so be sure to see your accountant when purchasing company assets.</p>
	<p>Debit Equipment (increases its balance)</p>
	<p>Credit Cash (decreases its balance)</p>
	<p>[Remember: A debit adds a positive number and a credit adds a negative number. 
	But you NEVER put a minus sign on a number you enter into the accounting software.]</p>
